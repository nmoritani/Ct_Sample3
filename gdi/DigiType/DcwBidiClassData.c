const DT32_int gBidiClass_ArraySize = sizeof(BidiClassArray) / sizeof(BidiClassArray[0]);

DT_BidiCls_Plane_Idx gBidiClass_ArrayPlaneIdx[256];

DT_void make_gBidiClass_ArrayPlaneIdx()
{
	DT16_ushort curplane, lastplane;
	DT32_int i;
	//DT_BiDiCls_Entry *pbidi = BidiClassArray;
	
	DT_memset(gBidiClass_ArrayPlaneIdx, -1, sizeof(gBidiClass_ArrayPlaneIdx));
	curplane = lastplane = BidiClassArray[0].code >> 8;
	for(i=0; i<gBidiClass_ArraySize; i++){
		if(-1==gBidiClass_ArrayPlaneIdx[BidiClassArray[i].code >> 8].idx_beg){
			lastplane= curplane;
			curplane = BidiClassArray[i].code >> 8;
			gBidiClass_ArrayPlaneIdx[curplane].idx_beg = i;
			gBidiClass_ArrayPlaneIdx[lastplane].idx_end = i-1;
		}
	}
	gBidiClass_ArrayPlaneIdx[curplane].idx_end = gBidiClass_ArraySize - 1;
}
