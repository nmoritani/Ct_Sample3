#define TABLE_SIZE(AR) (sizeof( AR )/sizeof( AR [0]))


static const struct
{
	VIE_MAPPING_TABLE * p_table;
	DT32_uint len;
}comp_table[5] = {
	{ map_table_0300, TABLE_SIZE(map_table_0300)},
	{ map_table_0301, TABLE_SIZE(map_table_0301)},
	{ map_table_0303, TABLE_SIZE(map_table_0303)},
	{ map_table_0309, TABLE_SIZE(map_table_0309)},
	{ map_table_0323, TABLE_SIZE(map_table_0323)},
};

/*
Binary search
*/
DT16_ushort Latin_Vietnamese(DT16_ushort idx, DT16_ushort A)
{													      
		VIE_MAPPING_TABLE * p_half_row = comp_table[idx].p_table;
		DT32_int length = comp_table[idx].len;
		DT32_int start, end, half_idx;;
		start=0;
		end = length-1;
		while(start <= end)
		{
			half_idx = (start+end) /2;
			if( (p_half_row+half_idx)->A <A ){
				start = half_idx+1;
			}
			else if( (p_half_row+half_idx)->A >A )
			{
				end = half_idx-1;
			}
			else if( (p_half_row+half_idx)->A == A )
			{
				return (p_half_row+half_idx)->B;
			}
		}
		return 0;
}
